while True:
    num = int(input("digite um numero de 1 a 7 (representado pelo dia da semana): "))
    if num == 1:
        print("Domingo.")
        break
    elif num == 2:
        print("Segunda.")
        break
    elif num == 3:
        print("Terça")
        break
    elif num == 4:
        print("Quarta.")
        break
    elif num == 5:
        print("Quinta.")
        break
    elif num == 6:
        print("Sexta.")
        break
    elif num == 7:
        print("Sábado.")
        break
    else:
        print("Erro,tente novamente.")